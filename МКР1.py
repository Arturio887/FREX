import random

def генерувати_випадкову_матрицю(рядки, стовпці):
    матриця = []
    for i in range(рядки):
        рядок = [random.randint(1, 99) for _ in range(стовпці)]
        матриця.append(рядок)
    return матриця

def головна_функція():
    while True:
        рядки = random.randint(3, 5)
        стовпці = random.randint(3, 5)
        
        матриця = генерувати_випадкову_матрицю(рядки, стовпці)
        
        print("Випадкова матриця:")
        for рядок in матриця:
            середнє_рядку = sum(рядок) / стовпці
            print(f"{рядок} - середнє значення рядку: {середнє_рядку:.2f}")

        середнє_значення_рядків = [(sum(рядок) / стовпці, рядок) for рядок in матриця]
        відсортовані_рядки = sorted(середнє_значення_рядків, key=lambda x: x[0])

        print("Відсортована матриця за зростанням середнього значення (за рядками):")
        for _, рядок in відсортовані_рядки:
            print(рядок)

        відповідь = input("Бажаєте завершити програму (так/ні)? ")
        if відповідь.lower() == "так":
            break

if __name__ == "__main__":
    головна_функція()



    
    
    
    


